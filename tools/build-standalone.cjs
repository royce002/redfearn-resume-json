/**
 * Transforms standalone.html (raw PHP render) into a self-contained, edit-friendly file.
 * - Inlines assets/css/resume.css
 * - Strips hydration JS (resume.js, INITIAL_RESUME_DATA, RC_BRAND_LOGOS) so manual edits stay put
 * - Strips third-party scripts (GA, LogRocket, Tawk.to)
 * - Rewrites CDN/absolute asset URLs to local relative paths
 * - Replaces data-rc-src lazy placeholders with real src attrs
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const SRC = path.join(ROOT, 'standalone.html');
const CSS = path.join(ROOT, 'assets', 'css', 'resume.css');

let html = fs.readFileSync(SRC, 'utf8');
const css = fs.readFileSync(CSS, 'utf8');

const before = html.length;

// --- Inline resume.css ------------------------------------------------------
html = html.replace(
  /<link rel="stylesheet" href="\/assets\/css\/resume\.css"\s*\/?>/i,
  () => `<style data-inlined="resume.css">\n${css}\n</style>`
);

// --- Drop hydration + third-party scripts -----------------------------------
const dropPatterns = [
  // Inline init blocks that hydrate the DOM
  /<script>\s*window\.INITIAL_RESUME_DATA[\s\S]*?<\/script>/gi,
  // External resume.js (and its preload)
  /<link rel="preload" href="\/assets\/js\/resume\.js"[^>]*>\s*/gi,
  /<script src="\/assets\/js\/resume\.js"[^>]*><\/script>\s*/gi,
  // Inline lazy-image priming script
  /<script>\s*\(function\s*\(\)\s*\{\s*var imgs = document\.querySelectorAll\("img\[data-rc-src\][\s\S]*?\}\)\(\);\s*<\/script>\s*/gi,
  // GA
  /<!-- Google tag \(gtag\.js\) -->[\s\S]*?gtag\("config"[\s\S]*?<\/script>\s*/gi,
  // LogRocket
  /<script src="https:\/\/cdn\.logr-in\.com[\s\S]*?<\/script>\s*<script defer>[\s\S]*?LogRocket\.init[\s\S]*?<\/script>\s*/gi,
  // Tawk.to (with surrounding comments)
  /<!--Start of Tawk\.to Script-->[\s\S]*?<!--End of Tawk\.to Script-->\s*/gi,
];
for (const re of dropPatterns) html = html.replace(re, '');

// --- Rewrite CDN URLs to local paths ----------------------------------------
html = html.replace(/https:\/\/assets\.redfearn\.co\/images\//g, 'assets/images/');
// Site-relative → relative (so `file://` works directly)
html = html.replace(/(src|href)="\/assets\//g, '$1="assets/');
html = html.replace(/data-rc-src="\/assets\//g, 'data-rc-src="assets/');

// --- Promote data-rc-src → src so deferred images actually load -------------
// Pattern: src="data:image/svg+xml,..." ... data-rc-src="REAL"
html = html.replace(
  /src="data:image\/svg\+xml[^"]*"\s+data-rc-src="([^"]+)"/g,
  (_m, real) => `src="${real}"`
);
// Strip any leftover data-rc-src attrs on already-loaded images
html = html.replace(/\s+data-rc-src="[^"]*"/g, '');

// --- Drop preconnect/dns-prefetch for things we no longer use ---------------
html = html.replace(/<link rel="preconnect" href="https:\/\/assets\.redfearn\.co"[^>]*>\s*/gi, '');
html = html.replace(/<link rel="preconnect" href="https:\/\/0\.gravatar\.com"[^>]*>\s*/gi, '');
html = html.replace(/<link rel="dns-prefetch" href="https:\/\/0\.gravatar\.com"[^>]*>\s*/gi, '');

// --- Mark file as standalone in <html> --------------------------------------
html = html.replace(/<html\s+lang="en"\s+data-theme="modern">/i,
  '<html lang="en" data-theme="modern" data-build="standalone">');

// --- Add an editor banner comment near the top ------------------------------
const banner = `<!--\n  STANDALONE EDIT BUILD — generated by tools/build-standalone.cjs\n  Source of truth lives in data/resume.json + index.php + config/render.php.\n  Edit this file freely; once aesthetics are dialed in, port changes back upstream.\n-->\n`;
html = html.replace(/<!DOCTYPE html>\s*/i, `<!DOCTYPE html>\n${banner}`);

fs.writeFileSync(SRC, html, 'utf8');
console.log(`standalone.html rewritten (${before} → ${html.length} bytes)`);
